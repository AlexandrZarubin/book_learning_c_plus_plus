#include <iostream>
#include <string>

using namespace std;

int main()
{
    setlocale(LC_ALL, "");
    const int MAX_ITEMS = 10; // Максимальное количество предметов в инвентаре
    string inventory[MAX_ITEMS]; // Создание массива строк для хранения предметов инвентаря

    int numItems = 0; // Текущее количество предметов в инвентаре
    inventory[numItems++] = "меч"; // Добавляем предмет "меч" в инвентарь
    inventory[numItems++] = "доспехи"; // Добавляем предмет "доспехи" в инвентарь
    inventory[numItems++] = "щит"; // Добавляем предмет "щит" в инвентарь

    // Вывод всех предметов в инвентаре
    cout << "Ваши предметы:\n";
    for (int i = 0; i < numItems; ++i)
    {
        cout << inventory[i] << endl; // Выводим каждый предмет на новой строке
    }

    // Замена меча на боевой топор
    cout << "\nВы меняете меч на боевой топор.";
    inventory[0] = "боевой топор"; // Замена первого предмета в инвентаре на "боевой топор"

    cout << "\nВаши предметы:\n";
    for (int i = 0; i < numItems; ++i)
    {
        cout << inventory[i] << endl; // Выводим обновленный инвентарь
    }

    // Вывод количества букв в названии первого предмета
    cout << "\nНазвание предмета '" << inventory[0] << "' состоит из ";
    cout << inventory[0].size() << " букв.\n";

    // Добавление найденного зелья исцеления
    cout << "\nВы находите зелье исцеления.";
    if (numItems < MAX_ITEMS) // Проверка, есть ли место в инвентаре
    {
        inventory[numItems++] = "зелье исцеления"; // Добавляем "зелье исцеления" в инвентарь
    }
    else
    {
        cout << "У вас слишком много предметов, и вы не можете нести еще один."; // Сообщение, если инвентарь полон
    }

    cout << "\nВаши предметы:\n";
    for (int i = 0; i < numItems; ++i)
    {
        cout << inventory[i] << endl; // Выводим инвентарь после добавления зелья
    }

    return 0; // Завершение программы
}
